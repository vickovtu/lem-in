#include "libft.h"

int	ft_my_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb == 0)
		return (0);
	while ((a * a) <= nb)
	{
		if ((a * a) == nb)
			return (a);
		a++;
	}
	return (0);
}
