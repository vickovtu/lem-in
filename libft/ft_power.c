#include "libft.h"

int	ft_power(int nb, int power)
{
	int i;

	i = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (power > 1)
	{
		i *= nb;
		power--;
	}
	return (i);
}
